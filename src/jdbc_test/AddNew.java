public ImageIcon resizeImage(String imagePath, byte[] pic){
          
        ImageIcon myImage=null;
        if(imagePath !=null)
        {
        myImage=new ImageIcon(imagePath);
        
        }else{
         myImage=new ImageIcon(pic);
        }
                
        Image img=myImage.getImage();
        Image img2=img.getScaledInstance(lblimage.getHeight(),    lblimage.getWidth(),  Image.SCALE_SMOOTH);
        ImageIcon image=new ImageIcon(img2);
        return image;
}
    public boolean verifyFields()
    {
        String species = txtspecies.getText();
        String genus = txtgenus.getText();
        
        // check empty fields
        if(species.trim().equals("") || genus.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Required Fields * Are Empty","Empty Fields",2);
            return false;
        }
        // if everything is ok
        else{
            return true;
        }
    }        
        
    public boolean checkName(String genus, String species){
        
        PreparedStatement st;
        ResultSet rs;
        boolean name_exist = false;
        
        String query = "SELECT * FROM `palm` WHERE `genus` = ? AND `species` = ?";
        
        try {
            
            st = My_CNX.getConnection().prepareStatement(query);
            st.setString(1, genus);
            st.setString(2, species);
            rs = st.executeQuery();
        
            if(rs.next())
            {
                name_exist = true;
                JOptionPane.showMessageDialog(null, "This Plant Already Exist in the Database", "Failed to Add...", 2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AddNew.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return name_exist;
    }
