    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String species = txtspecies.getText();
        String genus = txtgenus.getText();
        String common = txtcommon.getText();
        String stem = txtstem.getText();
        String leaf = txtleaf.getText();
        String location = txtlocation.getText();
        String date = ((JTextField)txtdate.getDateEditor().getUiComponent()).getText();

             // check if the data are empty
        if(verifyFields())
        {
        	//check if the common_name already exists
            if(!checkName(genus, species))
                {
                PreparedStatement ps;
                ResultSet rs;
                String registerSpecimenQuery = "INSERT INTO `palm`(`common_name`, `species`, `genus`, `c_stem`, `c_leaf`,`photo`,`location`,`date`) VALUES (?,?,?,?,?,?,?,?)";
            
                try {
                
                    ps = My_CNX.getConnection().prepareStatement(registerSpecimenQuery);
                    ps.setString(1, common);
                    ps.setString(2, species);
                    ps.setString(3, genus);
                    ps.setString(4, stem);
                    ps.setString(5, leaf);
                    ps.setBytes(6, pimage);
                    ps.setString(7, location);
                    ps.setString(8, date);
                    
                
                        if(ps.executeUpdate() != 0){
                            JOptionPane.showMessageDialog(null, "Your Specimen Has Been Added!");
                            AddNew add = new AddNew();
                            add.setVisible(true);
                            add.pack();
                            add.setLocationRelativeTo(null);
                            add.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                            this.dispose();
                        }else{
                            JOptionPane.showMessageDialog(null, "Error: Check Your Information");
                        }
                
                    }
            
                 catch (SQLException ex) {
                    Logger.getLogger(AddNew.class.getName()).log(Level.SEVERE, null, ex);
                }
           }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fchoser=new JFileChooser();
        fchoser.showOpenDialog(null);
        File f=fchoser.getSelectedFile();
        fname=f.getAbsolutePath();
        ImageIcon micon=new ImageIcon(fname);        
        try {
            File image=new File(fname);
            FileInputStream fis=new FileInputStream(image);
            ByteArrayOutputStream baos=new ByteArrayOutputStream();
            byte[] buf=new byte[1024];
            for(int readnum; (readnum=fis.read(buf)) !=-1;)
            {            
                baos.write(buf,0,readnum);                
            }
            pimage=baos.toByteArray();
            lblimage.setIcon(resizeImage(fname, buf));
        } catch (Exception e) {
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

